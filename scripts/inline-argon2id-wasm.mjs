#!/usr/bin/env node
// Regenerate src/crypto/argon2id-wasm.ts from the installed argon2id package.
// Run after bumping the argon2id dependency. Inlining keeps pow.ts working
// identically in browsers, web workers, and vitest (node) — no fetch path
// to special-case.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(here, '..');
const distDir = path.join(repoRoot, 'node_modules', 'argon2id', 'dist');
const out = path.join(repoRoot, 'src', 'crypto', 'argon2id-wasm.ts');

const simd = fs.readFileSync(path.join(distDir, 'simd.wasm')).toString('base64');
const noSimd = fs.readFileSync(path.join(distDir, 'no-simd.wasm')).toString('base64');

const body = `// Auto-generated by scripts/inline-argon2id-wasm.mjs. Do not edit by hand.
// Run \`node scripts/inline-argon2id-wasm.mjs\` to refresh after bumping the
// argon2id package version.

export const SIMD_WASM_BASE64 = ${JSON.stringify(simd)};
export const NO_SIMD_WASM_BASE64 = ${JSON.stringify(noSimd)};
`;

fs.writeFileSync(out, body);
console.log(`wrote ${out} (${simd.length + noSimd.length} base64 chars)`);
